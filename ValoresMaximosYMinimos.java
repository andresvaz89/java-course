public class ValoresMaximosYMinimos {
    public static void main(String[] args) {
        System.out.println("El valor máx de int: " + Integer.MAX_VALUE);
        System.out.println("El valor mín de int: " + Integer.MIN_VALUE);
        System.out.println("El valor máx de double: " + Double.MAX_VALUE);
        System.out.println("El valor mín de double: " + Double.MIN_VALUE);
        System.out.println("El valor máx de char: " + (int) Character.MAX_VALUE);
        System.out.println("El valor mín de char: " + (int) Character.MIN_VALUE);
    }
}
